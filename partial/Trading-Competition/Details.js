const detailsBtn = document.querySelectorAll('.detailsBtn');

function showHide() {
    let content = document.querySelectorAll(".text");
    if (content[0].style.display === "none") {
      content[0].style.display = "block"
      detailsBtn[0].innerText = "Hide";
    } else {
      content[0].style.display = "none";
      detailsBtn[0].innerText = "Details";
    }
  }


